       <li><span style="color:#F0761C;">What Is The Loan Amount Which Can Be Sanctioned?</span><br>
  	
HFCs sanction loan amounts is based on eligibility depending upon your repayment capacity (which takes into account your age, qualifications, assets, liabilities, stability of occupation, savings history) and according to your income. The maximum loan that can be sanctioned varies with housing finance companies and the generally, the maximum loan  amount is 80 to 85% of the cost of your home.</li>

     <li><span style="color:#F0761C;">When and how can an application be made for home loans?</span><br>
  	
An application for home loans can be made any time after you have decided to acquire/construct a property, even if the property has not been selected or the construction has not commenced. An application form along with the necessary documents has to be submitted to the respective finance company after which they would review the application and decide on its status.</li>


 <li><span style="color:#F0761C;">How do I obtain a housing/commercial property loan?</span><br>
  	
