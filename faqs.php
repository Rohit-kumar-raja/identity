 <li><span style="color:#F0761C;">How much time does it take to get an application processed and the loan getting sanctioned?</span><br>
  	
The processing of a home loan application, if the documents are in order takes around fifteen days and it takes another week for the company to check out the property papers and make the disbursement.</li>


 <li><span style="color:#F0761C;">If I am not eligible for loans can I take a joint loan with any of my relative?</span><br>
  	
If you are an individual - your spouse, your parents, or even your children can be your co-applicants and their incomes can be clubbed with your income to enhance the amount of loan you are eligible for. It makes sense therefore, that the co-owner of a property has to be a co-applicant, but a co-applicant need not be the co-owner of the property. If you are a partnership or private limited company, any one of the directors or partners can be your co-applicant.

</li>

 <li><span style="color:#F0761C;">What are the fees and charges payable and when are they payable?</span><br>
  	
Processing fees and administrative fees, both payable upfront; are the charges payable to the Housing finance companies for sanctioning of Housing loans. The processing fee (fees at the time of application) could range between 0.8% to of the loan amount applied for, and is generally levied to cover the costs incidental to the application. Once the loan is sanctioned, an administrative fee of 1% of the loan amount sanctioned will have to be paid.</li>


 <li><span style="color:#F0761C;">What is a fixed interest rate?</span><br>
  	
A Fixed interest rate for Home loans is one where the rate charged by the HFC on the loan amount is constant over the tenure of the loan. A fixed interest rate protects the borrower from a rise in home loan rates. While on the flip side, he may not benefit if the market rates were to fall. Therefore, it is advisable to go in for a fixed rate if you feel that the rate of interests in the market have touched rock bottom and the rates can only move upwards.</li>


 <li><span style="color:#F0761C;">What is a floating interest rate?</span><br>
  	
A Floating interest rate for home loans is a loan where the interest rate which is payable is linked to the market rate e.g. the bank lending rate. The interest rate payable by you will also rise and fall as per bank lending rates which may fluctuate.</li>


 <li><span style="color:#F0761C;">What are the repayment period options?</span><br>
  	
Repayment period options range generally from 5 to 15 years. A 20-year repayment  period option is also provided by a few HFC's usually at a higher interest rate.</li>


 <li><span style="color:#F0761C;">What is meant by an EMI (Equated Monthly Installment)?</span><br>
  	
An EMI refers to the fixed sum of money that you will be paying to the housing finance company every month against a loan amount borrowed for a fixed period of time. An EMI has two components, the principal component and the interest component. . The amount of the EMI depends on the quantum of loan, interest rate applicable and the term of the loan. The loan carrying the lower EMI for the same tenure is the cheaper option.</li>

 <li><span style="color:#F0761C;">Can one repay the loan ahead of schedule?</span><br>
  	
It is possible to repay a loan ahead of schedule. A form of a penalty termed as a pre-payment penalty is payable to certain institutions which varies from one HFC to another.</li>


 <li><span style="color:#F0761C;">Can a NRI avail of a housing loan? </span><br>
  	
Yes, NRIs can avail of a housing loan to buy a property in India. However, the terms and conditions for a NRI loan offered by the banks and HFCs are different than housing finance granted to Residents of India.</li>


