<li><span style="color:#F0761C;">Do NRI's require permission of Reserve Bank to transfer immovable property in India?</span><br>
  	
No. NRI do not require any permission to transfer any immovable property in India. Permission is required only in the case of transferring of agricultural or plantation property or farm house to another citizen of India NRI or PIO.
</li>

<li><span style="color:#F0761C;">Do PIO require permission of Reserve Bank to purchase immovable property in India for their residential use?</span><br>
  	
Reserve Bank has granted general permission to foreign citizens of Indian origin, whether resident in India or abroad, to purchase immovable property other than agricultural land/farm house/plantation property, in India. They are, therefore, not required to obtain separate permission of Reserve Bank or file any declaration.</li>



<li><span style="color:#F0761C;">Can such property be sold without the permission of Reserve Bank?</span><br>
  	
Yes. Reserve Bank has granted general permission for sale of such property. However, where another foreign citizen of Indian origin purchases the property, funds towards the purchase consideration should either be remitted to India or paid out of balances in non-resident accounts maintained with banks in India.</li>



<li><span style="color:#F0761C;">Can NRI/PIO rent out the properties (residential/commercial) if not required for immediate use?</span><br>
  	
