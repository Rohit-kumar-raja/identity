    <li class="about2"><span style="color:#F0761C;">What is a sale deed?</span><br>
  	
A sale deed, also called a “conveyance”, is a document which transfers immovable property be it land or a house, flat, office or other structure to another person. In almost all cases, the sale deed must be registered compulsorily except in the case of resale of units in existing cooperative societies where the state law grants a specific exemption from registration. Regardless, all sale deeds are liable for stamp duty and the rates vary from state to state. Also the duty depends upon various factors, such as age of building, location and type of unit and so on.</li>


 <li class="about2"><span style="color:#F0761C;">What is a title deed?</span><br>
  	
A title deed is a document that proves the right of a person to an immovable property. A person can acquire an immovable property by various means and a properly stamped and executed document evidencing the transaction is a title document. For example a sale deed, a release deed, a relinquishment deed, a gift deed, a family settlement deed, a partition deed, a will all are evidence of how a person has acquired an immovable property and may be called title deeds.</li>

 <li class="about2"><span style="color:#F0761C;">What is a conveyance deed? Why is it required?</span><br>
  	
The word conveyance means the transfer of ownership or interest in real property from one person to another by a document, such as a deed, lease, or mortgage. In India, transfer of property or rights in immovable property is governed by the Transfer of Property Act, 1882. For the transfer of any immovable property or rights in immovable property, it is necessary to execute a conveyance deed.

</li>

<li class="about2"><span style="color:#F0761C;">What is a building completion certificate?</span><br>
  	
A building completion certificate is the final document granted by the plan sanctioning authority and usually follows the occupancy certificate. This document certifies that all acts necessary in connection with the construction of a building are complete.

</li>

<li class="about2"><span style="color:#F0761C;">What is an occupancy certificate?</span><br>
  	
An occupancy certificate is granted by the plan sanctioning authority once the building is complete and ready for Inhabitation. In some places, an official water connection is granted only after the OC has been obtained. This document is given after verification that the construction has been carried out in accordance with the approved plans. The builder is not entitled to give possession and the unit buyer is not allowed to occupy the unit till the OC has been obtained. Further, the property comes into existence on and from the date of granting of OC. Property taxes are also levied as a unit from the OC date.</li>
